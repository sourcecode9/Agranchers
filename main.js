const year = document.querySelector('#year');
const navToggle = document.querySelector('.nav-toggle');
const siteNav = document.querySelector('#site-nav');
const contactForm = document.querySelector('.contact-form');
const languageOptions = document.querySelectorAll('.language-option');
const projectModal = document.querySelector('#projectModal');
const modalClose = document.querySelector('.modal-close');
const modalKicker = document.querySelector('#modalKicker');
const modalTitle = document.querySelector('#modalTitle');
const modalSummary = document.querySelector('#modalSummary');
const modalGallery = document.querySelector('#modalGallery');
const modalVideos = document.querySelector('#modalVideos');
let currentLanguage = 'en';

function isPlaceholderVideoId(videoId) {
  return videoId.startsWith('VIDEO_ID_');
}

function renderProjectVideos(videos) {
  const language = translations[currentLanguage];

  return videos
    .map((video) => {
      const title = currentLanguage === 'ar' ? video.titleAr : video.title;

      if (isPlaceholderVideoId(video.videoId)) {
        return `
          <div class="video-placeholder" aria-label="Placeholder video preview">
            <span>${language.video}</span>
            <strong>${title}</strong>
            <p>${language.replaceVideo.replace('{id}', video.videoId)}</p>
          </div>
        `;
      }

      return `
        <iframe
          class="video-frame"
          src="https://www.youtube.com/embed/${video.videoId}"
          title="${title}"
          loading="lazy"
          referrerpolicy="strict-origin-when-cross-origin"
          allow="accelerometer; autoplay; clipboard-write; encrypted-media; gyroscope; picture-in-picture; web-share"
          allowfullscreen
        ></iframe>
      `;
    })
    .join('');
}

function openProject(projectKey) {
  const project = projects[projectKey];

  if (!project || !projectModal) {
    return;
  }

  modalKicker.textContent = currentLanguage === 'ar' ? project.kickerAr : project.kicker;
  modalTitle.textContent = currentLanguage === 'ar' ? project.titleAr : project.title;
  modalSummary.textContent = currentLanguage === 'ar' ? project.summaryAr : project.summary;
  modalGallery.innerHTML = project.images
    .map(
      (image) => {
        const label = currentLanguage === 'ar' ? image.labelAr : image.label;

        return `
        <div class="modal-image" style="background-image: linear-gradient(180deg, transparent, rgba(4, 8, 5, 0.72)), url('${image.url}')">
          <span>${label}</span>
        </div>
      `;
      }
    )
    .join('');
  modalVideos.innerHTML = renderProjectVideos(project.videos);

  projectModal.showModal();
}

function setLanguage(languageCode) {
  const language = translations[languageCode] ? languageCode : 'en';
  currentLanguage = language;

  document.documentElement.lang = language;
  document.documentElement.dir = language === 'ar' ? 'rtl' : 'ltr';

  document.querySelectorAll('[data-i18n]').forEach((element) => {
    const key = element.dataset.i18n;

    if (translations[language][key]) {
      element.textContent = translations[language][key];
    }
  });

  document.querySelectorAll('[data-i18n-placeholder]').forEach((element) => {
    const key = element.dataset.i18nPlaceholder;

    if (translations[language][key]) {
      element.setAttribute('placeholder', translations[language][key]);
    }
  });

  languageOptions.forEach((button) => {
    const isActive = button.dataset.lang === language;
    button.classList.toggle('is-active', isActive);
    button.setAttribute('aria-pressed', String(isActive));
  });
}

if (year) {
  year.textContent = new Date().getFullYear();
}

if (navToggle && siteNav) {
  navToggle.addEventListener('click', () => {
    const isOpen = siteNav.classList.toggle('is-open');
    navToggle.setAttribute('aria-expanded', String(isOpen));
  });

  siteNav.querySelectorAll('a').forEach((link) => {
    link.addEventListener('click', () => {
      siteNav.classList.remove('is-open');
      navToggle.setAttribute('aria-expanded', 'false');
    });
  });
}

languageOptions.forEach((button) => {
  button.addEventListener('click', () => {
    setLanguage(button.dataset.lang);
  });
});

document.querySelectorAll('.snapshot-card').forEach((card) => {
  card.addEventListener('click', () => {
    openProject(card.dataset.project);
  });
});

if (modalClose && projectModal) {
  modalClose.addEventListener('click', () => {
    projectModal.close();
  });

  projectModal.addEventListener('click', (event) => {
    if (event.target === projectModal) {
      projectModal.close();
    }
  });
}

if (contactForm) {
  contactForm.addEventListener('submit', (event) => {
    event.preventDefault();
    contactForm.reset();
    window.alert(translations[currentLanguage].alert);
  });
}

setLanguage('en');
